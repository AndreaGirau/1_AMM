CREATE TABLE Conto(
    
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    balance REAL
);

CREATE TABLE Utente(
    
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(128),
    password VARCHAR(128),
    contoId INTEGER,
    tipo INTEGER,
    FOREIGN KEY (contoId) REFERENCES Conto(id)
);

CREATE TABLE Prodotto(

    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(128),
    description VARCHAR(128),
    price REAL,
    quantity INTEGER,
    imgUrl VARCHAR(128),
    venditoreId INTEGER,
    FOREIGN KEY (venditoreId) REFERENCES Utente(id)
);

